const listeners = new Set();

export function getLang() {
    const stored = localStorage.getItem(STORAGE_KEY);
    return stored === 'en' || stored === 'vi' ? stored : 'vi';
}

export function setLang(nextLang) {
    const lang = nextLang === 'en' ? 'en' : 'vi';
    localStorage.setItem(STORAGE_KEY, lang);
    document.documentElement.lang = lang;
    listeners.forEach((fn) => {
        try { fn(lang); } catch (_) { }
    });
    try { window.dispatchEvent(new Event('langchange')); } catch (_) { }
}

export function onLangChange(callback) {
    listeners.add(callback);
    return () => listeners.delete(callback);
}

export function t(key) {
    const lang = getLang();
    const dict = dictionaries[lang] || dictionaries.vi;
    return dict[key] || key;
}

// Initialize document language on load
(() => {
    const lang = getLang();
    document.documentElement.lang = lang;
})();

export default { t, getLang, setLang, onLangChange };


